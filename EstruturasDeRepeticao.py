
nomes = ["Junior", "Shey", "Pamonha"]
#Isso aqui "[]" é um vetor no qual guardou em um espaço a variavel "salvarNomes", no qual foi atribuido os nomes.
for salvarNomes in nomes:
    print(salvarNomes)
#Aqui tratamos do while, sabemos que ele interrompe a execucao quando o resultado passa a ser falso.
contadorDeNumeros = 1
while contadorDeNumeros < 21:
    print(contadorDeNumeros)
    contadorDeNumeros += 1
